# Скрипт для диагностики подключения устройства через USB

# Перезапуск сервера ADB
Write-Host "Перезапуск сервера ADB..."
adb kill-server
adb start-server

# Проверка списка устройств
Write-Host "Проверка подключенных устройств..."
adb devices

# Вывод подробной информации о подключении
Write-Host "Получение информации о подключении..."
adb usb

Write-Host "Диагностика завершена. Если устройство не отображается, проверьте настройки и повторите попытку."
